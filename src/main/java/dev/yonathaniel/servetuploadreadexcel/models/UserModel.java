package dev.yonathaniel.servetuploadreadexcel.models;

/**
 *
 * @author Aviator
 */
public class UserModel {

    private String name;
    private String town;
    private double age;

    public UserModel(String name, String town, double age) {
        this.name = name;
        this.town = town;
        this.age = age;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the town
     */
    public String getTown() {
        return town;
    }

    /**
     * @param town the town to set
     */
    public void setTown(String town) {
        this.town = town;
    }

    /**
     * @return the age
     */
    public double getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(double age) {
        this.age = age;
    }

}
